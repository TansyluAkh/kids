  List<String> generateWords(int difficulty, int numberOfQuestions) {
    List<String> words = [];

    switch (difficulty) {
      case 1:
        words = data['easy'];
        break;
      case 2:
        words = data['medium'];
        break;
      case 3:
        words = data['hard'];
        break;
    }
    return outputWords(words, numberOfQuestions);
  }
    
  //* Output final list of shuffled words
  List<String> outputWords(List<String> words, int numberOfQuestions) {
    return (words.toList()..shuffle()).sublist(0, numberOfQuestions);
  }

